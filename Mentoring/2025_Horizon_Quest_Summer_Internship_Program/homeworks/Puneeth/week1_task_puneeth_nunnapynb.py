def square_a_num():
  data = []
  amount_of_data = int(input("How many numbers do you wish to find the square of?"))
  for i in range(amount_of_data):
    number = int(input("What number do you wish to be squared?"))
    square = number ** 2
    data.append(square)
  print(data)

